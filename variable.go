package main

import "fmt"

func main() {
	var f_name string = "Damon"
	var l_name = "Salvatore"
	var num1 = 12
	var isAdult = true
	fmt.Println(f_name)
	fmt.Println(l_name)
	fmt.Println(num1)
	fmt.Println(isAdult)

	// shorthand syntax
	myName := "Faraz"
	fmt.Println(myName)

	// NOTE ::= you cannot use the sorthand syntax rule everywhere because if you want to initialize the variable but yo dont want to declare at the run time then shorthand syntax will not work
}
